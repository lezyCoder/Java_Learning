public class FORLOOP {
    public static void main(String[] args) {

        for (int num = 1; num < 11; num++) {
            System.out.println("Number is : " + num);
        }
    }
}

/*
The Java for loop is an entry control loop, meaning it checks the given condition before executing the loop body. 
Its structured syntax makes it more readable and concise compared to other loop structures like the while loop. 
*/ 